using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FunctionsExperiments
{
    public class TipCalculator
    {
        public decimal GetTipAmount(decimal cost, decimal tipPercent)
        {
            decimal tipAmount = cost * tipPercent;
            decimal totalBill = 0.0m;
            return totalBill = cost + tipAmount;
        }
    }
}
